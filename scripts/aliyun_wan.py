print(f"=== 提交 {len(PROMPTS)} 个片段 ===")
tasks = []

for i, prompt in enumerate(PROMPTS):
    clip_num = i + 1
    model = MODELS[i]
    body = json.dumps({
        "model": model,
        "input": {"prompt": prompt},
        "parameters": {"duration": 5, "size": "1280*720"}
    }).encode()

    for retry in range(3):
        try:
            resp = requests.post(
                BASE + "/services/aigc/videogeneration/text-to-video",
                data=body, headers=H, timeout=60
            ).json()
            tid = resp.get("output", {}).get("task_id")
            if tid:
                tasks.append((clip_num, tid, model))
                print(f"  [{clip_num:02d}/{len(PROMPTS)}] ✅")
                break
            else:
                print(f"  [{clip_num:02d}] {resp.get('message','')}")
        except Exception as e:
            if retry < 2:
                time.sleep(10)
            else:
                print(f"  [{clip_num:02d}] 失败: {str(e)[:50]}")
    time.sleep(3)

print(f"\n成功提交 {len(tasks)} 个")

