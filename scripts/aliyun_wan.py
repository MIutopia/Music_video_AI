            r = requests.get(
                BASE + "/tasks/" + tid,
                headers={"Authorization": "Bearer " + KEY}
            ).json()
            status = r.get("output", {}).get("task_status")

            if status == "SUCCEEDED":
                url = r.get("output", {}).get("video_url")
                if url:
                    data = requests.get(url, timeout=120).content
                    # 统一命名为 p01.mp4 ~ p40.mp4
                    fname = f"p{clip_num:02d}.mp4"
